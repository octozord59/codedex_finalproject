def add(x, y):
    total = x + y
    return total

def subtract(x, y):
    total = x - y
    return total

def multiply(x, y):
    total = x * y
    return total

def divide(x, y):
    total = x / y
    return total

def exp(x, y):
    total = x ** y
    return total

print(add(4, 2)) # 6
print(subtract(10, 4)) # 6
print(multiply(3, 2)) # 6
print(divide(12, 2)) # 6
print(exp(4, 2)) #4
